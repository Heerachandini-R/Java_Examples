package com.heera.java;

public class StringLength {

	public static void main(String[] args) {
		String str="Welcome";
		//To find length of a string using string.length() method
		System.out.println("Length of the String: "+str.length());
		//To find Index of a given character indexOf() method
		System.out.println("Index of character l: "+str.indexOf('l'));
	}
}