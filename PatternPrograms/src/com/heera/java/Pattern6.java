package com.heera.java;
public class Pattern6 {

	public static void main(String[] args) 
	{
		for(int i=1;i<5;i++) 
		{
			for(int j=1;j<=i;j++) 
			{
				System.out.print(i+ " "); //printing i means the no. of row
			}
			System.out.println();
		}
	}

}
