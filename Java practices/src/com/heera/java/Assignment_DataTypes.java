package com.heera.java;

public class Assignment_DataTypes {

	public static void main(String[] args) {

		int a = 10, b = 5;
		System.out.println("***Integer Datatype operations***");
		System.out.println("a+b  => " + (a + b));
		System.out.println("a-b => " + (a - b));
		System.out.println("a*b => " + (a * b));
		System.out.println("a/b => " + (a / b));

		System.out.println(a++); // Increment operator
		System.out.println(++a);

		System.out.println(b--); // Decrement operator
		System.out.println(--b);
		
		
		System.out.println("***Short Datatype operations***");
		
		short num1=12345,num2=12345;
		System.out.println("num1 + num2 => " + (num1+num2));
		System.out.println("num1 - num2 => " + (num1-num2));
		System.out.println("num1 * num2 => " + (num1*num2));
		System.out.println("num1 / num2 => " + (num1/num2));
		System.out.println(num1++);
		System.out.println(++num1);
		
		
		
		

	}

}
