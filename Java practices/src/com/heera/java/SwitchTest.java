package com.heera.java;

public class SwitchTest {

	public static void main(String[] args) {
		int n = 3;
		switch (n) {
		case 0:
			System.out.println("A");
			break;

		case 1:
			System.out.println("B");
			break;

		case 2:
			System.out.println("C");
			break;

		case 3:
			System.out.println("D");
			break;

		default:
			System.out.println("E");

		}

	}

}
